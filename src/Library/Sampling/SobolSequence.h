//  SobolSequence.h - Owen-scrambled Sobol (0,2)-sequence generator.
//
//    Implements the padded Sobol approach from Burley, "Practical
//    Hash-based Owen Scrambling" (JCGT 2020).  Only dimensions 0
//    and 1 of the Sobol sequence are used; higher dimensions are
//    obtained by reusing these two with different Owen scramble
//    seeds (the "padding" trick).  This preserves the (0,2)-net
//    stratification properties that make the first two Sobol
//    dimensions special, while providing independent samples in
//    each dimension group.
//
//    Convergence improves from O(N^-1) (independent sampling) to
//    O(N^-3/2 (log N)^(s-1)) for smooth integrands.
//
//  References:
//    - Burley, "Practical Hash-based Owen Scrambling", JCGT 2020
//    - Owen, "Randomly Permuted (t,m,s)-Nets and (t,s)-Sequences",
//      Monte Carlo and Quasi-Monte Carlo Methods, 1995
//    - Pharr, Jakob, Humphreys, "Physically Based Rendering" (4e),
//      Chapter 8: Sampling and Reconstruction
//
